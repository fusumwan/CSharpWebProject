    protected void SubmitBut_OnClick(object sender, EventArgs e)
    {
        long _lMrt_no;
        if (long.TryParse(mrt_no.Text, out _lMrt_no))
        {
            MobileNumberProfile _oMobileNumberProfile = new MobileNumberProfile(SYSConn<MSSQLConnect>.Connect());
            _oMobileNumberProfile.SetCid(RWLFramework.CurrentUser[this.Page].Uid);
            _oMobileNumberProfile.SetCdate(DateTime.Now);
            _oMobileNumberProfile.SetMrt_no(_lMrt_no);
            _oMobileNumberProfile.SetMrt_group(mrt_group.SelectedValue);
            _oMobileNumberProfile.SetPool(pool.SelectedValue);
            _oMobileNumberProfile.SetActive(true);
            _oMobileNumberProfile.SetStatus("AVALIABLE");
            if (MobileNumberProfileRepository.Insert(SYSConn<MSSQLConnect>.Connect(), _oMobileNumberProfile))
            {
                RegisterJavaScript(string.Empty, "jAlert('Create Success!', 'System Message');", true);
            }
            else
            {
                RegisterJavaScript(string.Empty, "jAlert('Create Fail!', 'System Message');", true);
            }
        }
    }
    #region RegisterJavaScript
    protected void RegisterJavaScript(string x_sID, string x_sScript, bool x_bIncludeScript)
    {
        if (x_bIncludeScript) x_sScript = "<script>" + x_sScript + "</script>";
        ScriptManager.RegisterStartupScript(this.Page, this.GetType(), x_sID + (new Random()).Next(1, 1000).ToString() + Guid.NewGuid().ToString(), x_sScript, false);
    }
    #endregion
